package algo101;

// 이클립스가 복붙해올때 자동으로 오류 날만한 곳에 \를 붙여줘서 문제의 의미가 없었음
// "'\를 출력하기 위해서는 앞에 \를 붙이자.
public class B25083 {

	public static void main(String[] args) {
		System.out.println("         ,r'\"7");
		System.out.println("r`-_   ,'  ,/");
		System.out.println(" \\. \". L_r'");
		System.out.println("   `~\\/");
		System.out.println("      |");
		System.out.println("      |");
	}

}
